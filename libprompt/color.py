# Zsh has a much nicer format for printing colors
class color_zsh:
	reset_all         = '%{\033[00m%}'
	class fg:
		grey          = '%F{0}'
		red           = '%F{1}'
		green         = '%F{2}'
		yellow        = '%F{3}'
		blue          = '%F{4}'
		magenta       = '%F{5}'
		cyan          = '%F{6}'
		light_gray    = '%F{7}'
		dark_gray     = '%F{8}'
		ligth_red     = '%F{9}'
		ligth_green   = '%F{10}'
		ligth_yellow  = '%F{11}'
		ligth_blue    = '%F{12}'
		ligth_magenta = '%F{13}'
		ligth_cyan    = '%F{14}'
		white         = '%F{15}'
		reset         = '%f'
	class bg:
		grey          = '%K{0}'
		red           = '%K{1}'
		green         = '%K{2}'
		yellow        = '%K{3}'
		blue          = '%K{4}'
		magenta       = '%K{5}'
		cyan          = '%K{6}'
		light_gray    = '%K{7}'
		dark_gray     = '%K{8}'
		ligth_red     = '%K{9}'
		ligth_green   = '%K{10}'
		ligth_yellow  = '%K{11}'
		ligth_blue    = '%K{12}'
		ligth_magenta = '%K{13}'
		ligth_cyan    = '%K{14}'
		white         = '%K{15}'
		reset         = '%k'
	class style:
		# Note: Most terminals and shells don't support some of these, so
		# you're better of only using bold and underlined text if you want
		# to share your theme with others.
		bold         = '%B'
		dim          = '%{\033[2m%}'
		underline    = '%U'
		blink        = '%{\033[5m%}'
		reverse      = '%S'
		hidden       = '%{\033[8m%}'
		no_bold      = '%b'
		no_dim       = '%{\033[22m%}'
		no_underline = '%u'
		no_reverse   = '%s'
		no_reverse   = '%{\033[28m%}'

